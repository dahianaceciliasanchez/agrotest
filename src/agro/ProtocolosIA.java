    private int traeCod(String sql) {
        int cod = 0;
        try {

            conn.traeDatos(sql);
            if (conn.resultado.next()) {
                cod = conn.resultado.getInt(1);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ProtocolosIA.class.getName()).log(Level.SEVERE, null, ex);

        }
        return cod;
    }
