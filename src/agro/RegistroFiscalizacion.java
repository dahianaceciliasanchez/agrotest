    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        try {
            txtidanimal.requestFocus();
            LimpiaProductos();
            String sql = "SELECT max(Nrofiscalizacion) + 1 as total from fiscalizacion";
            conn.traeDatos(sql);
            if (conn.resultado.next()) {
                txtnrof.setText(conn.resultado.getString("total"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegistroFiscalizacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MousePressed

