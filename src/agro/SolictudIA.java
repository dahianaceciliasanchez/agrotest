    private void cargaGrilla() {
        m.setRowCount(0);
        String sql = "Select id, NroSolicitud, Empleadoid,estado FROM solicitudia";
        String columna = "id";
        try {
            if (!txtfiltro.getText().trim().isEmpty()) {
                if (cbobuscar.getSelectedIndex() == 1) {
                    columna = "id";
                }
                sql = sql + " where " + columna + " like '%" + txtfiltro.getText().trim() + "%' ";
            }
            System.out.println(sql);
            conn.sentencia = conn.conexion.createStatement();
            conn.resultado = conn.sentencia.executeQuery(sql);

            while (conn.resultado.next()) {
                m.addRow(new Object[]{conn.resultado.getString("id"), conn.resultado.getString("Nrosolicitud"),
                    conn.resultado.getString("empleadoid"),
                    conn.resultado.getString("estado"),});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Razas.class.getName()).log(Level.SEVERE, null, ex);

        }
    }

    private void cargaCombo(javax.swing.JComboBox cbo, String sql) {

        try {
            cbo.removeAllItems();
            conn.sentencia = conn.conexion.createStatement();
            conn.resultado = conn.sentencia.executeQuery(sql);
            while (conn.resultado.next()) {
                cbo.addItem(conn.resultado.getString("Descripcion"));
            }
            cbo.setSelectedItem(-1);
        } catch (SQLException ex) {
            Logger.getLogger(SolictudIA.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

