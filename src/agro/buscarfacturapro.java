    private void Buscar() {
        String sql = "select d.id, d.facturaid, f.nrofactura, f.proveedorid, p.Nombre Nombre_Prove, "
                + "d.productosid, p2.Descripcion Descrip_Producto, "
                + "d.precio, d.cantidad, d.iva \n"
                + "from detallefactura d \n"
                + "inner join facturaproveedor f \n"
                + "on d.facturaid = f.id \n"
                + "inner join proveedores p \n"
                + "on f.proveedorid = p.id \n"
                + "inner join productos p2 \n"
                + "on d.productosid = p2.id"              
                + "where d.id = "+ ficha.getValueAt(ficha.getSelectedRow(), 0).toString();    
        System.out.println(sql);
        conn.traeDatos(sql);
        try {
            if (conn.resultado.next()) {
                txtidf.setText(conn.resultado.getString("id"));
                txtfa.setText(conn.resultado.getString("facturaid"));
                txtfactura.setText(conn.resultado.getString("nrofactura"));
                txtidproveedor.setText(conn.resultado.getString("proveedorid"));
                txtNombrep.setText(conn.resultado.getString("Nombre_Prove"));
                txtproductoid.setText(conn.resultado.getString("productosid"));
                txtNombrep.setText(conn.resultado.getString("Descrip_Producto"));
                txtprecios.setText(conn.resultado.getString("precio"));
                txtcantidades.setText(conn.resultado.getString("cantidad"));
                iva.setText(conn.resultado.getString("iva"));

            }
        } catch (SQLException ex) {
            Logger.getLogger(buscarfacturapro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

